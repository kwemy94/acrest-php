            <h3 id="specialite">Spécialité Marketing - Commerce - Vente</h3>
            <p id="texte">
                <strong>Objectif de la formation</strong><br><br>
                Cette spécialité vise à répondre à un besoin exprimé par les entreprises : s'entourer des commerciaux outillés, parfaitement imprégnés de la logique marketing, sensibilisés sur les besoins changeants du consommateur et orientés vers le développement des ventes dans un environnement caractérisé par la concurrence.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
                <strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> La compréhension de l'économie internationale ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> La maîtrise d'autres langues ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> L'aptitude à la négociation ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> La compréhension de l'environnement professionnel ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre capable de travailler sous pression ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> L'adaptabilité et polyvalence.

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Comprendre la logique marketing ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Comprendre les défis à relever par l'entreprise dans un environnement de concurrence ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Comprendre les déterminants du succès commercial de l'entreprise ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Vendre de manière durable et rentable ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Conduire une équipe commerciale vers l'atteinte des objectifs ;<br>
