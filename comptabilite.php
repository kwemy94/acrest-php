		 	<h3 id="specialite">Spécialité comptabilite</h3>
            <p id="texte">
            	<strong>Objectif de la formation</strong><br><br>
            	La spécialité Comptabilité et gestion des entreprises a pour but de munir les étudiants des connaissances et savoir-faire leur permettant de traduire de manière comptable, toutes les opérations commerciales ou financières et d'établir les documents correspondants, d'analyser les informations dont ils disposent pour préparer les décisions de gestion.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
            	<strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir une bonne compréhension de l'environnement économique et des entreprises ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser l'outil informatique ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser la communication écrite et orale ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre capable de diriger une équipe de travail.<br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Réaliser la gestion des opérations comptables, fiscales et sociales (tenue des livres comptables, élaboration des états financiers, ...) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Analyser la rentabilité des activités de l'organisation ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Elaborer les budgets et suivre leur exécution ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Centraliser, organiser et redresser les comptabilités des entreprises ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Collaborer efficacement avec le chef dans la gestion de l'entreprise ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Contrôler et planifier la production ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maitriser les logiciels de base de la comptabilité.
            </p>
            <h3>Débouchés</h3>
            <p id="texte">
            	<span class="glyphicon glyphicon-menu-right"></span> Comptable en entreprise (PME) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Collaborateur comptable en cabinet ;
                <span class="glyphicon glyphicon-menu-right"></span> Assistant comptable dans les grandes entreprises ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Gestionnaire de la paie ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Responsable comptable ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Trésorier ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Contrôleur de gestion ; Etc.
            </p>
            <h3>Diplôme exigé</h3>

            <p id="texte">
                Baccalauréat ABI, A, ACA, ACC, C, D, E, TI, F2, F3, F5, F8, CG, ESF, IH, HO, TO, SES, FIG, GCE A/L ou tout diplôme équivalent
            </p>
