            <h3 id="specialite">Spécialité Industrie du Textile et de l’Habillement</h3>
            <h4> <i><strong>Option:</strong> Industrie de l’habillement</i></h4>
            <p id="texte">
                <strong>Objectif de la formation</strong><br><br>
               Le BTS "Industrie de l'habillement" a pour objectif de former des créateurs des modèles de vêtements et d'accessoires pour le prêt-à-porter, la haute couture et dans tous les secteurs de l'industrie et du commerce liés à la création de mode. Le designer de mode suit toutes les étapes d'un projet, de sa conception à sa création. À partir d'une commande, il émet des hypothèses de travail puis procède à un choix conceptuel intégrant les données du marché. Ce choix aboutit ensuite à la réalisation d'un prototype. Le Métier de designer de mode, au-delà ses activités de création donne de la valeur ajoutée aux textiles, il stimule la consommation du textile et la sauvegarde des emplois dans l'agriculture pour les fibres textiles naturelles et l'industrie chimique pour les fibres artificielles ou synthétiques.

            </p>
