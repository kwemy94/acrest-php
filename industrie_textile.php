        <h2 class="presentation">BTS - Arts et Métiers de la Culture</h2>
        <p class="presentation">
            L'<strong>industrie textile</strong> rassemble l'ensembre des activités de conception, de fabrication et de commercialisation des textiles et donc, entre autres, de l'habillement. Cette industrie compte de très nombreux métiers tout au long d'une chaîne de fabrication composée des fabriquants de tissus et de tricots, des fabriquants de produits finis et de distributeurs, qui transforment des matières premières fibreuses en des produits semi-ouvrés ou entierement manufacturés. Les fabriquants de fibres naturelles et de fibres synthétiques intervient en amont, et donc en dehors de cette chaîne.
        </p>
         

