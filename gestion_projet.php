		 	<h3 id="specialite">Spécialité gestion_projet</h3>
            <p id="texte">
            	<strong>Objectif de la formation</strong><br><br>
            	La spécialité Gestion des projets vise à former des techniciens capables de conduire des projets d'entreprises de tout secteur (industriel, service, commercial, technologique, culturel), à travers le développement de la compréhension de l'entreprise et l'acquisition des connaissances théoriques et compétences pratiques du management de projet.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
            	<strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir une bonne compréhension de l'environnement économique et des entreprises ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser l'outil informatique ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser la communication écrite et orale ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre capable de diriger une équipe de travail.<br>

                <strong>Compétences spécifiques</strong><br>
                Innover, créer, améliorer un projet, un produit ou un procédé :<br>
                <span class="glyphicon glyphicon-menu-right"></span> Identifier les partenariats locaux, nationaux et internationaux ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Réaliser une veille technologique ou concurrentielle ;<br>
                Coordonner un projet :<br>
                <span class="glyphicon glyphicon-menu-right"></span> Réaliser une pré-étude ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Définir les objectifs opérationnels d’un projet ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Identifier les outils de travail ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Constituer des dossiers techniques ;<br>
                Rechercher et traiter l'information :
                <span class="glyphicon glyphicon-menu-right"></span> Identifier les sources à exploiter ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Analyser et synthétiser les informations trouvées.
            </p>
            <h3>Débouchés</h3>
            <p id="texte">
            	<span class="glyphicon glyphicon-menu-right"></span> Assistant au chef de projet ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Assistant marketing ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Responsable de la communication ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Planificateur de projet.
            </p>
            <h3>Diplôme exigé</h3>

            <p id="texte">
                Baccalauréat ABI, A, ACA, ACC, C, D, E, TI, F2, F3, F5, F8, CG, ESF, IH, HO, TO, SES, FIG, GCE A/L ou tout diplôme équivalent
            </p>
