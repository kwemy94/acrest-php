		 	<h3 id="specialite">Spécialité Production Cinématographique</h3>
            <p id="texte">
            	<strong>Objectif de la formation</strong><br><br>
            	Cette spécialité forme des spécialistes de l'élevage et de la filière animale, disposant de solides connaissances dans l'ensemble des techniques de production animale mais également en biologie et en chimie. L'étudiant apprend la conduite d'élevage sous tous ses aspects : qualité de l'alimentation, croissance des animaux, reproduction, manipulations et interventions sur les animaux, surveillance sanitaire, bien-être animal, conception des bâtiments...
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
            	<strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser les outils informatiques de base ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Développer une attitude professionnelle dans le respect de la déontologie et de l’éthique ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Travailler en équipe en milieu de formation et en milieu de pratique professionnelle ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Comprendre le fonctionnement des organisations ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Travailler dans un environnement multiculturel ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Créer et gérer une entreprise ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Développer progressivement une autonomie d'apprentissage afin de pouvoir poursuivre de façon continue son développement personnel et professionnel tout au long de sa carrière.<br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Fournir des conseils techniques à un chef d'exploitation ou à une coopérative aussi bien dans la production en elle-même que dans la gestion et le fonctionnement d'une exploitation agricole ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Gérer une exploitation agricole avec un élevage qu’il apprendra à conduire : qualité de l'alimentation, croissance, reproduction et bien-être des animaux, surveillance sanitaire, intervention et manipulation (insémination et soins, par exemple) des animaux ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Réaliser des diagnostics techniques, financiers, réglementaires et environnementaux concernant l’élevage ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Manipuler les machines et les équipements liés à l'élevage et à la production.<br>
            </p>
            <h3>Débouchés</h3>
            <p id="texte">
            	<span class="glyphicon glyphicon-menu-right"></span>  Chef d’exploitation <br>
                <span class="glyphicon glyphicon-menu-right"></span> Chef d'équipe ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Conducteur de travaux ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Responsable d'élevage ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Conseiller technique ou commercial.<br>
            </p>
            <h3>Diplôme exigé</h3>

            <p id="texte">
                Cette filière accueille les étudiants titulaire du BAC, GCE/AL ou un diplome équivalent.
            </p>
