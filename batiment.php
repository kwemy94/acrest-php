        <h3 id="specialite">Spécialité Bâtiment</h3>
        <p id="texte">
          Le BTS Bâtiment fore les étudiants à toutes les composantes de la construction, de l'étude technique et financière du projet à la préparation d'un budget prévisionnel, en passant par la conduite et le suivi du chantier.
        </p>

        <h3>Objectif du BTS Bâtiment</h3>
        <p id="texte">
          Le BTS Bâtiment amène ses titulaires à la maîtrise des compétences et techniques nécessaires à l'accomplissement des travaux rencontrés dans le bâtiment, mais églement à la compréhension des problèmes de droit, d'économie et de gestion qui accompagnent les activités du bâtiment. <br>
          Ses connaissances couvrent tant les nouvelles constructions que la réhabittion de l'existant (habitations privées et collectives, locaux commerciaux, installations publiques, ...). <br>
          Les fonctions du diplômé du <strong>BTS Bâtiment </strong>vont de l'étude technique et financière du projet à la préparation d'un budget prévisionnel, en passant par la conduite et le suivi du chantier.
        </p>

        <h3>Débouchés</h3>

        <p id="texte">
          <strong>Intégrer le marché du travail</strong><br>
          Le diplome de <strong>BTS Bâtiment </strong> vous assure un emploi. Vous exercerez dans les entreprises du bâtiment, dans les cabinets d'architectes, dans les bureaux d'étude et d'ingénieurie. Vous interviendrez en tant que <i>Chef de projet, Chef d'équipe </i> puis après quelques années d'expérience, <i>Conducteur de travaux</i>.<br>
          <strong>Poursuivre vos études</strong><br>
          Si votre souhait est de poursuivre vos études après votre <strong>BTS Bâtiment</strong>, il vous sera possible de reprendre un cycle universitaire, par exemple un MST en génie civil.
        </p>

        <h3>Diplôme exigé</h3>

        <p id="texte">
          Cette filière accueille les étudiants titulaire du BAC C, E, F4, GCE/AL ou un diplome équivalent.
        </p>
