      <h2 class="presentation">BTS - Génie Civil</h2>
      <p class="presentation">
        Le <strong>génie civil</strong> est l'ensembe des techniques deconstructions civiles. Les ingénieurs civils ou ingénieur en génie civil s'occupent de la conception, la réalisation, l'exploitation et la réhabilitation d'ouvrages de construction et d'infrastructures dont ils assurent la gestion afin de répondre aux besoins de la société, tout en assurant la sécurité du public et de la protection de l'environnement. Très variées, leur réalisations se repartissent principalement dans cinq grands domaines d'intervention: <strong>Structures, géotechnique, hydraulique, transport et environnement</strong><br><br>
