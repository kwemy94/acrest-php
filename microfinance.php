		 	<h3 id="specialite">Spécialité Microfinance</h3>
            <p id="texte">
            	<strong>Objectif de la formation</strong><br><br>
            	Cette formation vise à combler le déficit en nombre et en qualité en matière de ressources humaines dont les établissements de crédit ont besoin pour la conduite de leurs activités. En outre, elle fournit aux entreprises, quel que soit leur domaine d'activité, des collaborateurs pouvant leur permettre de tirer le maximum des opportunités que leur offre le système financier dans son évolution, son expansion et son arrimage à la finance mondiale.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
            	<strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir une bonne compréhension de l'environnement professionnel et économique ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser la communication écrite et orale ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre apte à la vente et à la négociation commerciale ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser le cadre juridique de l'activité et être apte à l'analyse des règles fiscales applicables;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser les TIC applicable à la banque ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre capable de prendre du recul face à une problématique donnée et de trouver la solution appropriée respectant à la fois l'attente du client et la politique commerciale de son établissement ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Faire preuve d'adaptabilité et de polyvalence.<br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Construire et développer une relation de confiance, personnalisée avec chaque client, dans le cadre de la politique commerciale arrêtée par son établissement ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Développer quantitativement et qualitativement un fonds de commerce, notamment en améliorant le taux d'équipement des clients en produits et services ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Contribuer, par son action personnelle, à un accueil de qualité ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Gérer et développer quantitativement et qualitativement un portefeuille de clientèle de professionnels dans le cadre de la politique commerciale arrêtée par son établissement ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Contribuer au développement de son établissement par son action commerciale auprès des clients professionnels ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Développer l'approche globale des clients professionnels ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Ouvrir et gérer les comptes ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Distribuer les produits et services attachés aux comptes ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Promouvoir et utiliser les technologies de transmission des informations ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Distribuer les produits d'épargne bancaires et non bancaires et de gestion de trésorerie ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Distribuer les produits liés à l'épargne financière et notamment ceux dits de gestion collective ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Promouvoir les crédits à la consommation, des crédits immobiliers aux particuliers et montage des dossiers ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Promouvoir les modes de financement du cycle d'exploitation et des investissements des entreprises et montage des dossiers ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Promouvoir les produits d'assurance (Bancassurance) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Le suivi et gestion des risques clients ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Conduire une analyse économique et financière de la situation du client, évaluation et suivi des risques.
            </p>
            <h3>Débouchés</h3>
            <p id="texte">
            	<span class="glyphicon glyphicon-menu-right"></span> Conseiller en microcrédit ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Chargé de clientèle ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Caissier ou guichetier ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Fondé de pouvoir d'EMF ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Analyste de microcrédit; Etc.

            </p>
