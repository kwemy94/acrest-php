			<h3 id="specialite">Spécialité Energie Renouvelable</h3>
            <P id="texte">
                Cette spécialité  pour objectif de former des experts dans la chaîne de la conception, la mise en service et la gestion d'un système énergétique à partir des phénomènes naturels réguliers ou constant, ou des données de la nature (les astre, le soleil, la lune et la terre).
            </P>

            <h3>Compétences & Débouchés</h3>
            <p id="texte">
                <strong>Compétences génétiques:</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Travailler en autonomie, collaborer en équipe ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Anlyser, synthétiser un document professionnel ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Participer à /Mener une démarche de gestion de projet. <br>
                <strong>Compétences spécifiques</strong> <br>
                <span class="glyphicon glyphicon-menu-right"></span> Faire une instllation électrique (de la production, réseaux de transport et distribution de l'énergie);<br>
                <span class="glyphicon glyphicon-menu-right"></span> Géner l'aspect technico-économique des réseaux électriques ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Mener et réaliser un projet ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Effectuer les travaux d'entretien et de maintenance dans les réseaux électriques. <br><br>
                Les débouchés sont nombreuses et dans tout type d'organisation: <br>
                <span class="glyphicon glyphicon-menu-right"></span> Chef de projet éolien et photovoltaïque;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Commercial en énergies rénouvelable;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Développeur de projets en énergies rénouvelables;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Installateur de panneaux solaires photovoltaïques; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Conseiller technique dans les agences de l'énergie; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Technicien du batiment en énergies rénouvelables. <br>
            </p>

            <h3>Diplôme exigé</h3>

            <p id="texte">
                Cette filière accueille les étudiants titulaire du BAC C, D, E, F2, F3, GCE/AL ou un diplome équivalent.
            </p>
