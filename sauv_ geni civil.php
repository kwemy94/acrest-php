      <div class="col-md-6 col-xs-12">
         <h3 id="specialite">Spécialité Maintenance des systèmes informatiques</h3>
        <p id="texte">
          Le BTS en Maintenance des Systèmes Informatique dote l’étudiant des compétences pour assurer l’intégrité, la sécurité et la pérennité d’un système informatique (ordinateursmaintenance logicielle et matérielle, réseaux informatiques) au sein d’une entreprise. Diagnostiquer et localiser la panne ou l’anomalie. Conseiller et apporter une assistance technique à l’utilisateur. Installer, configurer et administre un parc informatique (ordinateurs terminaux mobiles, périphériques, système d'exploitation et logiciels).
        </p>

        <h3>Débouchés</h3>

        <p id="texte">
          Le titulaire d’un BTS en Maintenance des Systèmes Informatiques peut exercer en tant que : Agent de maintenance, Chef d’équipe, Responsable maintenance de la cellule informatique, Consultant en informatique, Distributeur du matériel informatique, Responsable de La maintenance informatique et bureautique.
        </p>

        <h3>Diplôme exigé</h3>

        <p id="texte">
          Cette filière accueille les étudiants titulaire du BAC C, D, E, F2, F3, GCE/AL ou un diplome équivalent.
