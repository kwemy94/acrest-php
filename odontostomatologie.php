		 	<h3 id="specialite">Spécialité Odontostomatologie</h3>
            <p id="texte">
            	<strong>Objectif de la formation</strong><br><br>
            	Cette spécialité vise à former des thérapeutes dentaires hautement qualifiés capables de fournir des soins dentaires holistiques de haute qualité dans les cliniques au niveau local, national ou international. Elle cible les jeunes hommes et femmes qui ont la vocation et sont prêts non seulement à gagner leur vie mais aussi et surtout à sauver des vies.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
            	<strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre responsable et réflexif ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Disposer de capacités relationnelles ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Disposer d'une confiance et d'une assurance avérées ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir la capacité critique et de questionnement ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Développer une éthique et une normale professionnelles ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Proactivité et aptitude à prendre l'initiative ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir la pensée critique ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Etre capable de maintenir les normes morales professionnelles ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Disposer de compétences organisationnelles ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Faire des compromis et être à l'écoute ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Améliorer la productivité grâce à la gestion du temps.

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Fabrication et instrumentation de la denture ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Soins dentaires pour patients ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Administration de l'anesthésie locale en dentisterie ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Fabrication de denture ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Procédures chirurgicales dentaires ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Hygiène buccale ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Prise de l'histoire de la maladie (Interrogatoire) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Gestion des urgences dentaires à l'hôpital. P. Ex. Aspiration du corps étranger, syncope, etc. ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Aider le chirurgien dentaire à mener des procédures dentaires majeures, par exemple, l'immobilisation de la mandibule fracturée ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Prise de radiographies intra-orales ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Fabrication de prothèses complètes et partielles ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Manipulation des instruments ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Fonctionnement des équipements auxiliaires, y compris les produits chimiques des unités de rayons X ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Commande d'instruments de remplacement, fournitures et équipements.
            </p>
            <h3>Débouchés</h3>
