      	    <h3 id="specialite">Spécialité Menuiserie et Ebénisterie</h3>
            <P id="texte">
                Cette spécialité a pour objectif de former des techniciens supérieurs capables de développer, d'industrialiser des produits à base de bois et de résoudre les problèmes techniques liés à leur mise en oeuvre. Ils exercent leur métier en atelier ou sur chantier, aussi bien en construction neuve qu'en réhabitation ou en agencement. Ils travaillent le bois, ses dérivés et les matériaux associés (aluminium, produits verriers, mattières plastiques ...).
            </P>

            <h3>Compétences recherchées</h3>
            <p id="texte">
                <strong>Compétences génériques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Travailler en autonomie, collaborer en équipe ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Analyser, synthétiser un document (français, anglais) ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Participer à /Mener une démarche de gestion de projet ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Connaître et exploiter les réseaux professionnels et institutionnels des secteurs des travaux public. <br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Résoudre les problèmes techniques liés à la mise en oeuvre du bois et ses dérivés ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Participer aux études nécessaires à l'industrialistion et assurer les missions telles que la gestion et la production, l'organisation et planification, la gestion et l'amélioration de la qualité, la valorisation des ressources humaines en production ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Réaliser des ouvrages de menuiserie en bâtiment (escaliers, fermetures, cloisons, revêtements de sol, revêtement muraux...), d'agencement (magasins, salles de bains...), d'aménagent intérieur (mobilier, placards...) et de mobilier urbain (kiosques, aires de jeux...).<br>
                <span class="glyphicon glyphicon-menu-right"></span> Exploiter les données d'un site et produire les documents techniques en vue de la réalisation d'un projet d'aménagement; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser la faisabilité d'un projet d'aménagement urbain.
            </p>

            <h3>Débouchés</h3>
            <p id="texte">
                Cette spécialité vous offre la possibilité d'être: <br>
                <span class="glyphicon glyphicon-menu-right"></span> Agenceur/ceuse de cuisines et salles de bains; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Responsable de scierie; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Responsable d'ordonancement;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Technicien/ne de fabrication de mobilier et de  menuiserie; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Assistant qualité.
            </p>

            <h3>Diplôme exigé</h3>

            <p id="texte">
                Cette filière accueille les étudiants titulaire du BAC A, C, D, ACC, CGE, ACA ou un diplome équivalent.
            </p>
