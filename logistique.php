		 	<h3 id="specialite">Spécialité logistique</h3>
            <p id="texte">
            	<strong>Objectif de la formation</strong><br><br>
            	La spécialité Gestion logistique et transport vise à former des experts dans l'organisation et le management des opérations de transport et des prestations logistiques sur les marchés locaux, régionaux, nationaux et internationaux en tenant compte de la complémentarité des modes de transport et du développement durable. Ils maîtrisent à cet effet les langues étrangères, la communication et les techniques de négociation, les techniques de gestion et d'optimisation des flux de marchandise, la gestion des entrepôts ou des plates-formes ainsi que l'exploitation des réseaux de transports urbains.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
            	<strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir des connaissances générales de l'environnement social et économique national, régional et international ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Faire preuve de rigueur dans l'organisation du travail et une capacité de réactivité et de créativité ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir une connaissance générale des langues étrangères, l'anglais en particulier ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir le sens de la négociation, des relations commerciales et de la vente, comme de l'après- vente.<br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Assurer le pilotage d'une chaine logistique ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Connaître un large éventail des techniques liées à l'exploitation de la chaine logistique (entreposage, manutention, transitique, productique, transport, etc.) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Faciliter et coordonner l'échange entre les acteurs internes de l'entreprise ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Contribuer à la résolution rapide des problèmes entre fournisseurs et clients ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Gérer le changement et promouvoir des solutions nécessaires à l'adhésion des partenaires ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir une connaissance de la mercatique afin de cerner les attentes des consommateurs et à concevoir le meilleur compromis entre efficacité et qualité dans un contexte concurrentiel ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Concevoir des structures adaptables, en interaction permanente avec les multiples composantes de l'environnement ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Mettre en œuvre des méthodes à la fois souples et rationnelles, pour matérialiser son action et permettre la régulation des flux à travers le développement d'un système logistique et des réseaux d'information performants ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir des connaissances en gestion comptable et financière ainsi qu'en contrôle de gestion (plus centrées sur des outils de contrôle en temps réel que sur les méthodes comptables) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Connaître les outils nécessaires à l'optimisation de la qualité et de la sécurité des flux physiques et informationnels ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Prévoir des alternatives performantes en cas de perturbations des flux en cours ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Pouvoir utiliser des logiciels spécifiques, contribuer à leur choix par l'entreprise et favoriser leur exploitation ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Avoir des connaissances en gestion comptable et financière ainsi qu'en contrôle de gestion.
            </p>
