        <h2 class="presentation">BTS - Génie Biologique</h2>
        <p class="presentation">
            Le <strong>génie biologique</strong> désigne l'application des concepts et méthodes de la biologie (et accessoirement de la physique, chimie, mathématiques et informatique) afin de résoudre les problèmes relatifs aux sciences du vivant, en utilisant les méthodes analytiques et de synthèses propres à l'ingénierie ainsi que son expérience quant au coût et à la faisabilité d'une solution. Dans cette optique, alors que l'ingénierie traditionnelle applique les sciences physiques et mathématiques afin d'analyser, concevoir et fabriquer des outils, des structures et des processus non-vivants, le génie biologique utilise principalement le domaine des connaissances de la biologie moléculaire afin d'étudier et de promouvoir son application aux organismes vivants. <br><br>
