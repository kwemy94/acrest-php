            <h3 id="specialite">Spécialité Industrie Alimentaire</h3>
            <p id="texte">
                <strong>Objectif de la formation</strong><br><br>
                Cette spécialité vise à former des professionnels capables de transformer et de conserver les matières premières locales tout comme d’évaluer le danger microbiologique associé à la production des aliments.
            </p>
            <h3>COMPETENCES RECHERCHEES</h3>
            <p id="texte">
                <strong> Compétences génériques</strong><br><br>
                <span class="glyphicon glyphicon-menu-right"></span> Travailler en autonomie, collaborer en équipe ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Analyser, synthétiser un document professionnel (français, anglais) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Communiquer à l’oral, à l’écrit, en entreprise ou extérieur (français, anglais) ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Participer à /Mener une démarche de gestion de projet ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Connaître et exploiter les réseaux professionnels et institutionnels des secteurs biologiques.<br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Mettre en œuvre et contrôler les opérations de transformation, de fabrication des produits alimentaires ou biologiques ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Gérer et planifier l’ensemble des moyens humains et matériels dans un contexte d’hygiène et de sécurité ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Surveiller la qualité des matières premières et des produits tout au long des transformations ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Définir de nouveaux équipements ou procédés pour optimiser le processus de qualités des produits ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Prendre en charge ou participer à la démarche qualité de l’entreprise (animation, certification ISO, transformation, audit,...).
            </p>
