            <h3 id="specialite">Spécialité Urbanisme</h3>
        
            <h3>Objectif du BTS Urbanisme</h3>
            <p id="texte">
                Cette spécialité conduit à la formation des professionnels capables de concevoir et de conduire des actions cohérentes dans les domaines de l'habitat, des équipements, des espaces publics et du développement communal ou intercommunal, en concertation avec les collectivités locales dont il est le conseil.
            </p>

            <h3>Compétences recherchées</h3>
            <p id="texte">
                <strong>Compétences génériques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Travailler en autonomie, collaborer en équipe ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Analyser, synthétiser un document (français, anglais) ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Participer à /Mener une démarche de gestion de projet ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Connaître et exploiter les réseaux professionnels et institutionnels des secteurs des travaux publics. <br>

                <strong>Compétences spécifiques</strong><br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser les paramètres d'études et de la conduite des travaux d'aménagement urbain en bureau d'étude et sur le site ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser l'évolution démographique en vue de la précision des infrastructures et des équipements en milieu urbain ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Sensibiliser et former à la résolution des problèmes de dégradation de l'environnement ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Exploiter les données d'un site et produire les documents techniques en vue de la réalisation d'un projet d'aménagement ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Maîtriser la faisabilité d'un projet d'aménagement urbain.
            </p>
            <h3>Débouchés</h3>

            <p id="texte">
                <span class="glyphicon glyphicon-menu-right"></span> Chef de chantier ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Conducteur de travaux ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Dessinateur projecteur BTP ; <br>
                <span class="glyphicon glyphicon-menu-right"></span> Responsble des prix ;<br>
                <span class="glyphicon glyphicon-menu-right"></span> Chargé d'affaires.
            </p>

            <h3>Diplôme exigé</h3>

            <p id="texte">
                Cette filière accueille les étudiants titulaire du BAC C, D, E, F, GCE/AL ou un diplome équivalent.
            </p>
